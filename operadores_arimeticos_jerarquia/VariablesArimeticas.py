#variables numericas
num1 = 10
num2 = 5

print(num1)
print(num2)

#operaciones arimeticas
#suma
print(num1 + num2)
#resta
print(num1 - num2)
#multiplicacion
print(num1 * num2)
#divicion
print(num1 / num2)
#divicion entera
print(num1 // num2)
#exponencia
print(num1 ** num2)
#divicion residual
print(num1 % num2)

#operaciones mediante variables
suma = num1 + num2
print(suma)

resta = num1 - num2
print(resta)
